% Очистка памяти и закрытие старых окон
clear; clc; close all;

disp('Читаем файл signal_data.csv...');

% 1. Читаем данные из CSV (пропускаем первую строчку с текстом заголовка)
data = readmatrix('signal_data.csv', 'NumHeaderLines', 1);

% Извлекаем колонки
time_s = data(:, 1);       % Время в секундах
signal = data(:, 2);       % Значение QPSK сигнала

% 2. Рассчитываем параметры дискретизации
dt = time_s(2) - time_s(1); % Шаг времени (должен быть 1e-10 или 0.1 нс)
Fs = 1 / dt;                % Частота дискретизации (10 ГГц)
L = length(signal);         % Количество точек

disp(['Частота дискретизации: ', num2str(Fs/1e9), ' ГГц']);
disp(['Количество точек: ', num2str(L)]);

% 3. Быстрое преобразование Фурье (FFT)
Y = fft(signal);
P2 = abs(Y / L);            % Двухсторонний спектр
P1 = P2(1:floor(L/2)+1);    % Односторонний спектр
P1(2:end-1) = 2 * P1(2:end-1);

% Формируем ось частот для графика
f = Fs * (0:(L/2)) / L;

% 4. Рисуем графики
figure('Name', 'QPSK Analysis', 'Color', 'w', 'Position', [100, 100, 1000, 600]);

% --- ВЕРХНИЙ ГРАФИК: Временная область ---
subplot(2, 1, 1);
plot(time_s * 1e9, signal, 'b', 'LineWidth', 1);
title('Временная область: QPSK Сигнал ("Hello world")');
xlabel('Время (нс)');
ylabel('Амплитуда');
grid on;
% Ограничиваем график по оси X (показываем только первые 100 наносекунд)
% Чтобы увидеть скачки фазы, а не сплошное синее пятно
xlim([0 100]); 

% --- НИЖНИЙ ГРАФИК: Частотная область (Спектр) ---
subplot(2, 1, 2);
plot(f / 1e6, P1, 'r', 'LineWidth', 1.5);
title('Частотная область: Спектр сигнала');
xlabel('Частота (МГц)');
ylabel('Амплитуда |P1(f)|');
grid on;
% Ограничиваем спектр от 0 до 1000 МГц (1 ГГц)
xlim([0 1000]); 

disp('Готово! Графики построены.');